const month = 4

switch (month) {
    case 1:
        console.log("jan");
        
        break;
    case 2:
        console.log("feb");
            
        break;
    case 3:
        console.log("mar");
        
        break;
    case 4:
        console.log("apr");
        
        break;

    default:
        console.log("not match!");
        
        break;
}

